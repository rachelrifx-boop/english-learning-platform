// 缓存字典以提高性能
const commonPhonetics = getCommonPhoneticsDictionary()

/**
 * 快速获取句子音标（使用本地词库）
 */
export function getQuickSentencePhonetics(sentence: string): SentencePhonetics {
  // 移除标点符号，保留单词
  const words = sentence.match(/\b[\w']+\b/g) || []

  const wordPhonetics: Array<{ word: string; ipa: string }> = []

  for (const word of words) {
    if (!word) continue
    const lowerWord = word.toLowerCase()
    const ipa = commonPhonetics[lowerWord] || ''
    wordPhonetics.push({
      word,
      ipa
    })
  }

  // 生成完整音标字符串
  const phonetic = wordPhonetics
    .map(wp => {
      // 如果是标点符号后的大写字母开头，保留原词
      if (wp.word === wp.word.toUpperCase() && wp.word.length > 0) {
        return wp.ipa ? `${wp.word} /${wp.ipa}/` : wp.word
      }
      return wp.ipa ? `${wp.word} /${wp.ipa}/` : wp.word
    })
    .join(' ')

  return {
    original: sentence,
    phonetic,
    words: wordPhonetics
  }
}
