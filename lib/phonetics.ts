/**
 * 获取单词的IPA音标
 * 使用免费字典API获取音标数据
 */

export interface WordPhonetic {
  word: string
  ipa: string
  audio?: string
}

export interface SentencePhonetics {
  original: string
  phonetic: string
  words: Array<{
    word: string
    ipa: string
  }>
}

/**
 * 从DictionaryAPI获取单词音标
 */
async function getWordPhonetic(word: string): Promise<string | null> {
  try {
    const response = await fetch(`https://api.dictionaryapi.dev/api/v2/entries/en/${word}`)

    if (!response.ok) {
      return null
    }

    const data = await response.json()

    if (data && data.length > 0 && data[0].phonetics) {
      // 查找包含音标的phonetic
      const phoneticWithText = data[0].phonetics.find((p: any) => p.text)
      return phoneticWithText?.text || null
    }

    return null
  } catch (error) {
    console.log(`获取单词 ${word} 音标失败:`, error)
    return null
  }
}

/**
 * 为句子生成音标标注
 * 每个单词后面用 /.../ 显示音标
 */
export async function generateSentencePhonetics(sentence: string): Promise<SentencePhonetics> {
  // 移除标点符号，保留单词
  const words = sentence.match(/\b[\w']+\b/g) || []

  const wordPhonetics: Array<{ word: string; ipa: string }> = []

  for (const word of words) {
    if (!word) continue
    const ipa = await getWordPhonetic(word.toLowerCase())
    wordPhonetics.push({
      word,
      ipa: ipa || ''
    })
  }

  // 生成完整音标字符串
  const phonetic = wordPhonetics
    .map(wp => wp.ipa ? `${wp.word} /${wp.ipa}/` : wp.word)
    .join(' ')

  return {
    original: sentence,
    phonetic,
    words: wordPhonetics
  }
}

